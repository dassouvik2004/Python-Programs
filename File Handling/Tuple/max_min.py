myTuple = (0,20,30,40,50)
print("MyTuple: ",myTuple)
print("Max = ",max(myTuple))
print("Min = ",min(myTuple))
print("Sum = ",sum(myTuple))
print("Sorted List: ",sorted(myTuple))
print("all(myTyple): ",all(myTuple)) #Returns True if all element in the tuple is true. If the tuple is empty, returns False
print("any(myTyple): ",any(myTuple)) #Returns True if any element in the tuple is true. If the tuple is empty, returns False
